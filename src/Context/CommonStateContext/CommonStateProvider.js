// ---------------------------------------------------------------Imports-----------------------------------------------------------------
import React, { useState } from 'react';
import { CommonStateContextObject } from './CommonStateContextObject';
// ----------------------------------------------------------------------------------------------------------------------------------------



export const CommonStateProvider = ({ children }) => {

    // ---------------------------------------------------------------States-----------------------------------------------------------------
    const [openNavbar, setOpenNavbar] = useState(false);
    // --------------------------------------------------------------------------------------------------------------------------------------
    // ---------------------------------------------------------------Hooks-----------------------------------------------------------------
    // --------------------------------------------------------------------------------------------------------------------------------------
    // ---------------------------------------------------------------Functions--------------------------------------------------------------
    const changeNavbarState = () => {
        setOpenNavbar(!openNavbar)
    }
    // --------------------------------------------------------------------------------------------------------------------------------------
    return (
        <CommonStateContextObject.Provider value={{ changeNavbarState, openNavbar }}>
            {children}
        </CommonStateContextObject.Provider>
    )
}

